def nelio_oikein(sudoku: list, row: int, column: int):
    numbers = []
    for r in range(row, row + 3):
        for c in range(column, column + 3):
            number = sudoku[r][c]
            if number > 0 and number in numbers:
                return False
            numbers.append(number)
    return True


